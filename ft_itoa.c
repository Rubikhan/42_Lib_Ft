#include "libft.h"

char	*ft_itoa(int n)
{
	int		b;
	char	*a;

	b = 0;
	if (n == -2147483648)
	{
		a = ft_strnew(12);
		a = ft_strcpy(a, "-2147483648");
		return (a);
	}
	if (n < 0)
	{
		n = n * -1;
		b++;
	}
	b = (b + (int)ft_intlen(n));
	a = ft_strnew(b);
	if (a == NULL)
		return (NULL);
	b--;
	while (n > 9)
	{
		a[b--] = ((n % 10) + '0');
		n = (n / 10);
	}
	a[b] = (n + '0');
	if (b-- == 1)
		a[b] = '-';
	return (a);
}
